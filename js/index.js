


    function addClassToImage(element) {
        var innerImage = document.getElementById('ima01');
      if(innerImage) {
          if(innerImage.classList.contains('clicked')) {
        innerImage.classList.remove('clicked');
        }
        else {
              innerImage.classList.add('clicked');
        }
      }
    }

    
